<?php

# Caracteres de Escape
# Os caracteres de escape são usado para escrever caracateres especias, como é o caso de alguns símbolos que podem entrar em conflito com o código.

$frase =  "I'm Marcelo";
$frase2 =  'Você é o "Marcelo" que estuda PHP';

# a \ é símbolo para definir um caracter de escape 
$frase =  'I\'m Marcelo';
$frase2 =  "Você é o \"Marcelo\" que estuda PHP";
